func main() {
	block := flag.String("block", "", "Block start to crawl (Required)")
	flag.Parse()

	if *block == "" {
		fmt.Println("Block is required")
		os.Exit(1)
	}

	result := make(chan []*bsc.LogProcessed)
	fromBlock := make(chan string, 10000)
	defer func() {
		close(fromBlock)
		close(result)
	}()

	go getData(result, fromBlock)
	fromBlock <- "14190339"
	for i := range result {
		process(i, fromBlock)
	}

	select {}
}

func getData(result chan []*bsc.LogProcessed, fromBlock chan string) {
	c := bsc.NewClient(APIKey)
	for {
		block := <-fromBlock
		if block != "" {
			data, err := c.GetLogs(block, nil, Address)
			if err != nil {
				fmt.Println("Error - ", err)
				fromBlock <- block
				continue
			}
			if len(data) == 0 {
				fromBlock <- block
				continue
			}
			result <- data
		}
	}
}

func process(result []*bsc.LogProcessed, fromBlock chan string) {
	for i, v := range result {
		if i == len(result)-1 {
			fromBlock <- v.BlockNumber.Int().String()
		}
		go printRow(v)
	}
}

func printRow(data *bsc.LogProcessed) {
	str := fmt.Sprintf("%v| %4v | %6.2f BNB| %5vs ago | %v | %v", data.RoundId.Int().String(), data.Bet, data.Price, data.TimeBefore, data.BlockNumber.Int().String(), data.Address)
	if _, ok := shark[data.Address]; ok {
		fmt.Println(string(colorYellow), str)
	} else if data.Price >= defaultPriceNormal {
		if data.Bet == Up {
			fmt.Println(string(colorBlue), str)
		} else {
			fmt.Println(string(colorRed), str)
		}
	} else {
		fmt.Println(string(colorReset), str)
	}
}
